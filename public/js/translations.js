// Template helper: t("key", { var: value })
function t(key, vars) {
  const lang = (typeof UI !== "undefined" && UI.getLanguage) ? UI.getLanguage() : "en";
  let str = (Translations[lang] && Translations[lang][key]) || Translations.en[key] || key;
  if (vars) {
    Object.keys(vars).forEach((k) => {
      str = str.replace(`{${k}}`, vars[k]);
    });
  }
  return str;
}
